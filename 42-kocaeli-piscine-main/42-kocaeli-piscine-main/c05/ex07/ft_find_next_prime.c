int	is_prime(int nb)
{
	int	i;

	i = 2;
	while (i <= (nb / 2) && i <= 46341)
	{
		if (nb % i == 0)
			return (0);
		i++;
	}
	return (1);
}

int	ft_find_next_prime(int nb)
{
	if (nb <= 2)
		return (2);
	while (nb < 2147483647 && !is_prime(nb))
		nb++;
	return (nb);
}
